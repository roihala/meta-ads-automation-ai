def _run(cmd: list[str]) -> dict:
    proc = subprocess.run(cmd, capture_output=True, text=True, encoding="utf-8")
    if proc.returncode != 0:
        sys.stderr.write(f"\n!!! command failed (exit {proc.returncode}): {' '.join(cmd[:6])}...\n")
        sys.stderr.write(f"STDERR: {proc.stderr}\n")
        sys.stderr.write(f"STDOUT: {proc.stdout}\n")
        raise SystemExit(1)
    return json.loads(proc.stdout)


def log_decision(
    run_id: str,
    node_name: str,
    decision_type: str,
    summary: str,
    rationale: str | None = None,
    **extra: object,
) -> None:
    cmd = [
        "python",
        "-m",
        "campaigner.tools.log_decision",
        "--business-id",
        BUSINESS_ID,
        "--run-id",
        run_id,
        "--graph-name",
        "consultant_rescan",
        "--node-name",
        node_name,
        "--decision-type",
        decision_type,
        "--summary",
        summary,
    ]
    if rationale:
        cmd += ["--rationale", rationale]
    for k, v in extra.items():
        if v is None:
            continue
        if isinstance(v, (dict, list)):
            v = json.dumps(v, ensure_ascii=False)
        cmd += [f"--{k.replace('_', '-')}", str(v)]
    _run(cmd)


def propose(
    run_id: str,
    *,
    task_type: str,
    target_kind: str,
    target_id: str,
    payload: dict,
    rationale: str,
    urgency: str,
    expected_impact: dict | None = None,
) -> str | None:
    cmd = [
        "python",
        "-m",
        "campaigner.tools.propose_task",
        "--business-id",
        BUSINESS_ID,
        "--run-id",
        run_id,
        "--task-type",
        task_type,
        "--target-kind",
        target_kind,
        "--target-id",
        target_id,
        "--payload",
        json.dumps(payload, ensure_ascii=False),
        "--rationale",
        rationale,
        "--urgency",
        urgency,
    ]
    if expected_impact:
        cmd += ["--expected-impact", json.dumps(expected_impact, ensure_ascii=False)]
    result = _run(cmd)
    aid = result.get("approval_id") or (result.get("data") or {}).get("approval_id")
    if aid:
        print(f"  ✓ {task_type:20s} urgency={urgency:7s} approval_id={aid}")
    else:
        print(f"  ~ {task_type:20s} urgency={urgency:7s} ({result})")
    return aid


